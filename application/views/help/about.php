<h1>Tentang TokoPoko</h1>
<br />
<p>
	TokoPoko adalah cara baru bagi anda pemilik bisnis untuk mendapatkan pengunjung yang lebih banyak ke online store anda. Dengan menghubungkan account Facebook, Twitter, dan Instagram anda ke account TokoPoko, anda akan lebih mudah untuk menyebarluaskan aktifitas toko atau bisnis anda. 
</p>
<p>	
	Selain mudah karena semuanya dikerjakan untuk anda secara otomatis, anda juga diberikan keuntungan dari kenyataan bahwa orang2 yang datang ke TokoPoko berpotensi lebih besar untuk menjadi pembeli dibandingkan dengan pemakai2 Facebook, Twitter, atau Instagram yang biasanya tidak memberikan perhatian kepada postingan anda di timeline mereka.
</p>
<p>
	TokoPoko dirancang untuk menjadi sarana penjual dan pembeli untuk berkumpul di satu media. Selain menguntungkan penjual, TokoPoko memudahkan pembeli dan juga orang2 di luar sana untuk mencari sesuatu yang mereka cari dan butuhkan. Hal ini pun akan menguntungkan penjual karena semakin banyak orang yang mengunjungi TokoPoko, semakin besar pula kesempatan penjual untuk mengiklankan toko atau bisnis anda!
</p>