<h1>Cara Pakai</h1>
<br />
<p>
	TokoPoko dirancang khusus untuk memudahkan penjual dan pembeli untuk mencari atau mengiklankan produk atau bisnisnya. Cukup <a href="/auth/register">sign up</a> dengan mengisi formulir kami yang sangat pendek dan hubungkan account2 social media anda di halaman <a href="/account/facebook">account</a> anda.
</p>
<p>
	Setelah anda berhasil menghubungkan account social media anda, postingan2 terbaru anda di account itu akan otomatis tergabung ke dalam halaman utama TokoPoko. Jangan panik bila anda tidak melihat postingan anda sesaat setelah anda membuat postingan tersebut karena TokoPoko "bekerja" sekali dalam 10 menit untuk mempercepat akses anda ke <a href="/">halaman utama</a>.
</p>